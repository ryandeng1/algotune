import gzip


class Solver:
    def solve(self, problem):
        """
        Compress the plaintext using the gzip algorithm with mtime=0.

        :param problem: The problem dictionary generated by `generate_problem`.
        :return: A dictionary containing 'compressed_data'.
        """
        compress = gzip.compress
        # Use local compression function for a tiny speedup
        plaintext = problem["plaintext"]
        return {"compressed_data": compress(plaintext, compresslevel=9, mtime=0)}