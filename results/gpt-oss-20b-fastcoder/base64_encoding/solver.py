import base64

class Solver:
    def solve(self, problem: dict[str, bytes]) -> dict[str, bytes]:
        """
        Encode the plaintext using the Base64 algorithm.

        Args:
            problem (dict): The problem dictionary generated by `generate_problem`.

        Returns:
            dict: A dictionary containing 'encoded_data'.
        """
        # Extract plaintext and encode it directly
        return {'encoded_data': base64.b64encode(problem['plaintext'])}